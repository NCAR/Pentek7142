#include "p71xx.h"
#include <fcntl.h>
#include <sys/ioctl.h>
#include <iostream>
#include <stdio.h>


using namespace Pentek;

////////////////////////////////////////////////////////////////////////////////////////
p71xx::p71xx(std::string devName):
_ok(true),
_devName(devName)
{

	_ok = false;
	 _devCtrl = devName + "/ctrl";
	int fd = open(_devCtrl.c_str(), O_RDWR);
	if (fd < 0)
		return;
	close(fd);
	_ok = true;
}

////////////////////////////////////////////////////////////////////////////////////////
p71xx::~p71xx() {
}

////////////////////////////////////////////////////////////////////////////////////////
bool
p71xx::ok() {
	return _ok;
}

